#include "3-calc.h"

/**
 * op_add - adding
 * @a: integer
 * @b: integer
 * Return: result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substracting
 * @a: integer
 * @b: integer
 * Return: result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplicating
 * @a: integer
 * @b: integer
 * Return: result
 */


int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - dividing
 * @a: integer
 * @b: integer
 * Return: result
 */


int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo
 * @a: integer
 * @b: integer
 * Return: result
 */


int op_mod(int a, int b)
{
	return (a % b);
}
